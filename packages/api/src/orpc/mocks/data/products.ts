import type { PromoTag, PromoTagType } from "../../schemas/common.schema";
import type { Product } from "../../schemas/product.schema";

/** Canonical localized labels for each promo tag type (momo wording). */
const PROMO_LABELS: Record<PromoTagType, string> = {
  FLASH_SALE: "一日秒殺價",
  COUPON: "領券折",
  MO_COIN: "贈mo幣",
  LOWEST: "破盤下殺",
  BUY_ONE_GET_ONE: "買一送一",
  FREE_SHIPPING: "$290免運",
  NEW: "新品上市",
};

const tag = (type: PromoTagType): PromoTag => ({
  type,
  label: PROMO_LABELS[type],
});

const image = (id: string) =>
  `https://picsum.photos/seed/${encodeURIComponent(id)}/600/600`;

interface Seed {
  id: string;
  name: string;
  brand?: string;
  price: number;
  originalPrice?: number;
  categoryId: string;
  tags?: PromoTagType[];
  soldOut?: boolean;
  rating?: number;
  sales?: number;
}

const build = (seed: Seed): Product => {
  const discountRate =
    seed.originalPrice && seed.originalPrice > seed.price
      ? Math.round((1 - seed.price / seed.originalPrice) * 100)
      : undefined;
  return {
    id: seed.id,
    name: seed.name,
    brand: seed.brand,
    image: image(seed.id),
    price: seed.price,
    originalPrice: seed.originalPrice,
    discountRate,
    tags: (seed.tags ?? []).map(tag),
    soldOut: seed.soldOut ?? false,
    rating: seed.rating,
    sales: seed.sales ?? 0,
    categoryId: seed.categoryId,
  };
};

